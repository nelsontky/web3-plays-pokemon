import { WalletAdapterNetwork } from "@solana/wallet-adapter-base";
import {
  ConnectionProvider,
  WalletProvider,
} from "@solana/wallet-adapter-react";
import { WalletModalProvider } from "@solana/wallet-adapter-react-ui";
import {
  PhantomWalletAdapter,
  SolflareWalletAdapter,
  SolletExtensionWalletAdapter,
  SolletWalletAdapter,
  TorusWalletAdapter,
} from "@solana/wallet-adapter-wallets";
import { clusterApiUrl } from "@solana/web3.js";
import React, { ReactNode, useMemo } from "react";

require("@solana/wallet-adapter-react-ui/styles.css");

const SolanaContext = ({ children }: { children: ReactNode }) => {
  const network = WalletAdapterNetwork.Mainnet;

  const endpoint = process.env.NEXT_PUBLIC_RPC_URL ?? clusterApiUrl(network);

  const wallets = useMemo(
    () => [
      new PhantomWalletAdapter(),
      new SolflareWalletAdapter(),
      new SolletWalletAdapter({ network }),
      new SolletExtensionWalletAdapter({ network }),
      new TorusWalletAdapter(),
    ],
    // eslint-disable-next-line react-hooks/exhaustive-deps
    [network]
  );

  return (
    <ConnectionProvider
      endpoint={endpoint}
      config={
        process.env.NEXT_PUBLIC_RPC_CONFIG
          ? JSON.parse(process.env.NEXT_PUBLIC_RPC_CONFIG)
          : undefined
      }
    >
      <WalletProvider wallets={wallets} autoConnect>
        <WalletModalProvider>{children}</WalletModalProvider>
      </WalletProvider>
    </ConnectionProvider>
  );
};

export default SolanaContext;
